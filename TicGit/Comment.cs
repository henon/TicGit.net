using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.IO;

namespace TicGit.Net
{
    public class Comment
    {
        public string Text { get; private set; }
        public string User { get; private set; }
        public DateTime Time { get; private set; }

        public Comment(string text)
        {
            Text = text;
        }

        private Base m_base;
        public Comment(Base b, string filename)
        {
            m_base = b;
            Text = File.ReadAllText(filename);
            var tokens = Path.GetFileName(filename).Split('_');
            //type, date, user = 
            Time = GitSharp.Core.Util.DateTimeExtensions.UnixTimeToDateTime(Convert.ToInt64(tokens[1]));
            User = tokens[2];
        }
    }
}
